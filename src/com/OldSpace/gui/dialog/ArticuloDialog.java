    /*funciones para crear el combo box de categorias*/
    DefaultComboBoxModel<Categoria> modeloComboCat = new DefaultComboBoxModel<>();
    
    private void cargarModeloComboCat(){
        DAOCategoria dao = DAOCategoriaImpl.getInstancia();
        List<Categoria> listaCat = dao.listarCategorias();
        for (Categoria categoria : listaCat) {
            modeloComboCat.addElement(categoria);
        }
    }
    
    /*funciones para crear el combo box de proveedores*/
    DefaultComboBoxModel<Proveedor> modeloComboProve = new DefaultComboBoxModel<>();
    
    private void cargarModeloComboProve(){
        DAOProveedor dao = DAOProveedorImpl.getInstancia();
        List<Proveedor> lista = dao.listarProveedores();
        for (Proveedor proveedor : lista) {
            modeloComboProve.addElement(proveedor);
        }
    }
    
    
