    File archivoImagen;
    private void lblImageArticleMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblImageArticleMousePressed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivo jpg, png o gif", "jpg","png","gif");
        
        chooser.setFileFilter(filtro);
        int valor = chooser.showOpenDialog(this);
        
        if(valor == JFileChooser.APPROVE_OPTION){
            int anchoImagen = lblImageArticle.getWidth();
            int altoImagen = lblImageArticle.getHeight();
            
            archivoImagen = chooser.getSelectedFile();
            
            ImageIcon icono = new ImageIcon( archivoImagen.getAbsolutePath());
            Image imagen = icono.getImage();
            imagen.getScaledInstance(anchoImagen,altoImagen, Image.SCALE_DEFAULT);
            
            ImageIcon iconoRedimensionado = new ImageIcon(imagen);
            
            lblImageArticle.setIcon(iconoRedimensionado);
        }
    }//GEN-LAST:event_lblImageArticleMousePressed

    private void btnSaveArtiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveArtiActionPerformed
        // TODO add your handling code here:
        Producto producto = new Producto();
        Categoria cat = (Categoria)cbCategory.getSelectedItem();
        producto.setNombre(txtName.getText());
        producto.setPrecio(Float.parseFloat(txtPrice.getText()));
        producto.setStock(Short.parseShort(txtStock.getText()));
        producto.setIdCategoria(cat.getIdCategoria());
        producto.setUsuario((short)2);
        
        DAOProducto dao = DAOProductoImpl.getInstancia();
        short idProducto = dao.insertarProducto(producto);
        JOptionPane.showMessageDialog(this, "Se ingreso un producto con el Codigo: " + idProducto);
    }//GEN-LAST:event_btnSaveArtiActionPerformed

